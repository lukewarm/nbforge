<script setup>
import { ref, reactive, computed, onMounted } from 'vue'
import { useRouter, useRoute } from 'vue-router'
import { useAuthStore } from '@/stores/auth'
import { getApiUrl } from '@/utils/env'

const router = useRouter()
const route = useRoute()
const authStore = useAuthStore()
const loading = ref(false)
const error = ref(null)

const formData = reactive({
  email: '',
  password: '',
  rememberMe: false
})

// OAuth configuration
const oauthConfig = ref({
  google: false,
  github: false
})
const emailsEnabled = ref(false)

const hasOAuthProviders = computed(() => {
  return oauthConfig.value.google || oauthConfig.value.github
})

// Get the API URL
const API_URL = getApiUrl()

// Fetch configuration on component mount
onMounted(async () => {
  try {
    console.log('Login view - loading auth configuration')
    // Fetch auth configuration from backend
    const response = await fetch(`${API_URL}/auth/config`)
    const config = await response.json()
    
    console.log('Auth config loaded:', config)
    
    // Update OAuth config
    oauthConfig.value = {
      google: !!config.oauth_providers?.google?.client_id,
      github: !!config.oauth_providers?.github?.client_id
    }
    
    // Update email settings
    emailsEnabled.value = config.emails_enabled || false
    
    // Check for demo mode
    if (config.demo_mode) {
      console.log('Demo mode detected in Login view, setting up demo auth')
      authStore.demoMode = true
      
      try {
        // Get demo token directly
        const tokenResponse = await fetch(`${API_URL}/auth/demo-token`)
        if (!tokenResponse.ok) {
          throw new Error(`Failed to get demo token: ${tokenResponse.status}`)
        }
        
        const tokenData = await tokenResponse.json()
        console.log('Demo token obtained')
        
        // Store token
        authStore.token = tokenData.access_token
        localStorage.setItem('token', tokenData.access_token)
        
        // Fetch user
        await authStore.fetchCurrentUser()
        
        console.log('Demo authentication successful, redirecting to home')
        router.push({ name: 'home' })
      } catch (error) {
        console.error('Demo authentication failed:', error)
      }
    } else {
      console.log('Standard auth mode - showing login form')
    }
  } catch (error) {
    console.error('Failed to load auth configuration:', error)
  }
})

async function handleLogin() {
  loading.value = true
  error.value = null
  
  try {
    await authStore.login({
      email: formData.email,
      password: formData.password,
      rememberMe: formData.rememberMe
    })
    
    // Redirect to the originally requested page or home
    const redirectPath = route.query.redirect || '/'
    router.push(redirectPath)
  } catch (err) {
    error.value = err.message || "Login failed. Please check your credentials."
  } finally {
    loading.value = false
  }
}

// OAuth login methods
function loginWithGoogle() {
  window.location.href = `${API_URL}/auth/oauth/google`
}

function loginWithGithub() {
  window.location.href = `${API_URL}/auth/oauth/github`
}
</script> 